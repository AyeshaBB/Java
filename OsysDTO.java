public class OsysDTO{

	private int osysId;
	private String osysName;
	private double osysPrice;
	private String osysType;

	public void setOsysId(int osysId){
	    this.osysId = osysId;
	}
	public int getOsysId(){
	    return osysId;
	}

	public void setOsysName(String osysName){
	    this.osysName = osysName;
	}
	public String getOsysName(){
	    return osysName;
	}

	public void setOsysPrice(double osysPrice){
	    this.osysPrice = osysPrice;
	}
	public double getOsysPrice(){
	    return osysPrice;
	}

	public void setOsysType(String osysType){
	    this.osysType = osysType;
	}
	public String getOsysType(){
	    return osysType;
	}
}